using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace Loops_Statement
{
    class Demo_ForLoop
    {
        static void Main(string[] args)
        {
            int Count;
            for (Count = 1; Count <=10; Count++) {
                System.Console.WriteLine(Count);
            }
            System.Console.ReadLine();                                                                                                                                                        
        }
    }
}
